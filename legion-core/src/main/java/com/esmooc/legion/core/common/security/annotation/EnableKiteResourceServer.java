package com.esmooc.legion.core.common.security.annotation;

import com.esmooc.legion.core.common.security.component.KiteResourceServerAutoConfiguration;
import com.esmooc.legion.core.common.security.component.KiteSecurityBeanDefinitionRegistrar;
import org.springframework.context.annotation.Import;
import org.springframework.security.config.annotation.method.configuration.EnableGlobalMethodSecurity;
import org.springframework.security.oauth2.config.annotation.web.configuration.EnableResourceServer;

import java.lang.annotation.Documented;
import java.lang.annotation.ElementType;
import java.lang.annotation.Inherited;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * @author kite kite
 * @date 2018/11/10
 * <p>
 * 资源服务注解
 */
@Documented
@Inherited
@EnableResourceServer
@Target({ ElementType.TYPE })
@Retention(RetentionPolicy.RUNTIME)
@EnableGlobalMethodSecurity(prePostEnabled = true)
@Import({ KiteResourceServerAutoConfiguration.class, KiteSecurityBeanDefinitionRegistrar.class })
public @interface EnableKiteResourceServer {

}
